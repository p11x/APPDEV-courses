// ApplicationTypesDemo - Types of Java Applications
// Java can be used for various types of applications

public class ApplicationTypesDemo {
    
    public static void main(String[] args) {
        System.out.println("=== TYPES OF JAVA APPLICATIONS ===\n");
        
        System.out.println("--- 1. Standalone Applications ---");
        System.out.println("Desktop applications that run locally");
        System.out.println("Examples: Eclipse IDE, IntelliJ, Media Players");
        System.out.println("Technologies: AWT, Swing, JavaFX");
        
        System.out.println("\n--- 2. Web Applications ---");
        System.out.println("Server-side applications accessed via browser");
        System.out.println("Examples: E-commerce sites, Banking apps");
        System.out.println("Technologies: Servlets, JSP, Spring MVC");
        
        System.out.println("\n--- 3. Enterprise Applications ---");
        System.out.println("Large-scale business applications");
        System.out.println("Examples: ERP, CRM systems");
        System.out.println("Technologies: EJB, JPA, JTA, Spring Boot");
        
        System.out.println("\n--- 4. Mobile Applications ---");
        System.out.println("Android applications");
        System.out.println("Examples: WhatsApp, Instagram (Android)");
        System.out.println("Technologies: Android SDK, Kotlin, Jetpack Compose");
        
        System.out.println("\n--- 5. Distributed Applications ---");
        System.out.println("Applications running across multiple systems");
        System.out.println("Examples: Cloud services, Microservices");
        System.out.println("Technologies: RMI, EJB, Web Services, REST API");
        
        System.out.println("\n--- 6. Embedded Systems ---");
        System.out.println("Applications running on embedded devices");
        System.out.println("Examples: Smart cards, ATMs");
        System.out.println("Technologies: Java ME, IoT platforms");
        
        System.out.println("\n--- Angular Integration ---");
        System.out.println("Java (Spring Boot) + Angular = Full-stack web apps");
        System.out.println("Java handles: REST API, Business logic, Database");
        System.out.println("Angular handles: UI, User interaction, HTTP calls");
    }
}
