# Example320: Practice with Math
import math

# Math functions
print("Math Functions:")
print(f"Ceil: {math.ceil(3.2)}")
print(f"Floor: {math.floor(3.8)}")
print(f"Factorial: {math.factorial(5)}")
print(f"GCD: {math.gcd(12, 18)}")
print(f"Pi: {math.pi}")
print(f"E: {math.e}")

# Pow and sqrt
print("\nPow/Sqrt:")
print(f"Pow: {math.pow(2, 3)}")
print(f"Sqrt: {math.sqrt(16)}")

# Trig
print("\nTrig:")
print(f"Sin: {math.sin(math.pi/2)}")
print(f"Cos: {math.cos(0)}")
